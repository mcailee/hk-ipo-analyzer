def get_investors(code):
    """获取指定股票的基石投资者列表"""
    entry = CORNERSTONE_MAP.get(code)
    return entry["investors"] if entry else []

def get_all_investors():
    """获取所有不重复的基石投资者"""
    all_inv = set()
    for entry in CORNERSTONE_MAP.values():
        all_inv.update(entry["investors"])
    return sorted(all_inv)

def get_stocks_by_investor(investor_name):
    """获取某个基石参与的所有股票代码"""
    codes = []
    for code, entry in CORNERSTONE_MAP.items():
        if investor_name in entry["investors"]:
            codes.append(code)
    return codes
